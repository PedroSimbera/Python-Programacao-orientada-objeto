# Classe:
# Uma classe é uma estrutura que define um tipo de objeto. 
# Ela serve como um "modelo" ou "plano" a partir do qual você pode criar instâncias (objetos) desse tipo. 
# Uma classe define as propriedades e comportamentos comuns a todos os objetos dessa classe. 
# Por exemplo, você pode ter uma classe chamada "Carro" que define as propriedades e métodos comuns a todos os carros, como cor, marca, modelo, acelerar, frear, etc.

# Atributos:
# Os atributos são as características ou propriedades de um objeto. Eles representam o estado de um objeto. 
# Em Python, os atributos são definidos dentro de uma classe e podem ser acessados ​​e modificados usando a notação de ponto. 
# Por exemplo, um objeto da classe "Carro" pode ter atributos como cor, marca, modelo, ano, etc. Esses atributos armazenam informações específicas de cada objeto.

# Métodos:
# Os métodos são funções definidas dentro de uma classe que representam os comportamentos dos objetos dessa classe. 
# Eles definem as ações que os objetos podem realizar. 
# Por exemplo, uma classe "Carro" pode ter métodos como acelerar(), frear(), ligar(), desligar(), etc. 
# Os métodos são usados ​​para interagir com os atributos do objeto ou executar operações específicas relacionadas à classe.